    private void tbProductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbProductMouseClicked
        int select = tbProduct.getSelectedRow();//set number row
        for (int i = 0; i < pro.size(); i++) {
            if (i == select) {
                String a = ptm.findProductName(pro.get(i).getPtype());
                String b = pbm.findProductName(pro.get(i).getBrand());
                txtPID.setText(String.valueOf(pro.get(i).getPid()));//set product ID
                txtPName.setText(pro.get(i).getPname());//set product name
                txtPBrand.setText(b);//set product brand
                txtPPrice.setText(String.valueOf(pro.get(i).getPrice()));//set product price
                txtPSize.setText(pro.get(i).getSize());//set product size
                txtPType.setText(a);//set product type dong nay can sua
            }
        }
        //can't edit 
        txtPName.setEnabled(false);
        txtPBrand.setEnabled(false);
        txtPPrice.setEnabled(false);
        txtPPrice.setEnabled(false);
        txtPSize.setEnabled(false);
        txtPType.setEnabled(false);
        TypeCombo.setEnabled(false);
        BrandCombo.setEnabled(false);

    }//GEN-LAST:event_tbProductMouseClicked
