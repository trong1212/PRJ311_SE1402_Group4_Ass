    public void loadData() {
        dtm.setRowCount(0);//set table row equal 0
        pro = new ArrayList<>();//create new list product
        try {
            pro = pm.loadProduct();//load product 
            probrands = pbm.loadProductBrand();//load product brand
            protypes = ptm.loadProductType();//load product type
            for (int i = 0; i < pro.size(); i++) {
                String type = ptm.findProductName(pro.get(i).getPtype());
                String brand = pbm.findProductName(pro.get(i).getBrand());
                Object[] row = {pro.get(i).getPid(), pro.get(i).getPname(), type, pro.get(i).getPrice(), pro.get(i).getSize(), brand};
                dtm.addRow(row);//add data to table row
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Can't load data.", "Error", JOptionPane.ERROR_MESSAGE);//display warning message
        }

    }

